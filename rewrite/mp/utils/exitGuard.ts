// tabBar 页误触退出提醒单源（2026-07-13 用户反馈）。
// 病：tabBar 根页（首页/购物车/我·页面栈=1）「返回=退出小程序」，误触安卓实体返回键/iOS 侧滑手势易误退。
// 机制：wx.enableAlertBeforeUnload 是官方唯一「返回操作时弹原生确认框」的接口——覆盖安卓实体返回键、
//   左上角返回按钮、iOS 侧滑返回。开启后处于本页时按这些「返回」路径会先弹确认框再决定去留。
// 真机覆盖边界（根因#8「构建过≠真机能用」·机器守不了·须真机验）：
//   ✓ 安卓实体返回键/返回手势——最主要的误触退出路径，覆盖最可靠；
//   ~ iOS 侧滑返回——随微信版本，需真机确认；
//   ✗ 胶囊「×」关闭 / 手机 home 键 / 上滑杀进程——平台无任何钩子，永远拦不到（微信刻意不让小程序困住
//     用户，无法 100% 拦所有退出路径）。这是平台上界，不是本实现缺陷。
// 决策留痕：旧线（待办与债 2026-07-13）曾判「退出二次确认平台不可能」——系只凭社区说法、从未真用该 API；
//   本实现按官方机制落地，边界如实标注、不糊假弹窗（不做 onBackPress+「再按一次退出」toast：WebView/原生页
//   无 onBackPress 钩子·根页返回无法自绘拦截，唯一可用的就是本接口的原生确认框）。
// 基础库 2.12.0+ 才有该 API：typeof 兜底，老版本静默降级（不报错、不提醒·可接受·极少数老机型）。
const EXIT_ALERT_MESSAGE = '确定要离开小棉鸭吗？'

/** tabBar 页 onShow 调用：开启「返回即退出」二次确认（幂等·重复调用只重设文案·非 tab 切换不触发）。 */
export function armExitAlert(): void {
  if (typeof wx.enableAlertBeforeUnload === 'function') {
    wx.enableAlertBeforeUnload({ message: EXIT_ALERT_MESSAGE })
  }
}
